package com.company;
// TYPECASTING IN JAVA
public class Ollie1
{
    public static void main(String[] args)
    {
        // DATA TYPES IN JAVA
        short a = 10;
        a = (short) (a*10);     // TYPECASTING IN JAVA
        System.out.println(a);
        int b = 10;
        b = b*10;
        System.out.println(b);
        byte c = 10;
        System.out.println(c);
        c = (byte)(c*10);       // TYPECASTING IN JAVA
        System.out.println(c);

    }
}
